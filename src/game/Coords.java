package game;

/**
 * A coordinate pair.
 * 
 * @author littlewoo
 *
 */
public class Coords {
	public final int x, y;
	
	/**
	 * Create a new coordinate pair.
	 * 
	 * @param x the x coordinate
	 * @param y the y coordinate
	 */
	public Coords(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
}
